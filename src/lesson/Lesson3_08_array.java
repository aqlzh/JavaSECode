package lesson;

public class Lesson3_08_array {
    public static void main(String[] args) {
        int[] nums = new int[10] ;
        double[] n = new double[10] ;
        for (int i = 0; i <nums.length ; i++) {
            System.out.println(nums[i]) ;    //0
        }

        for (int i = 0; i <n.length ; i++) {
            System.out.println(n[i])  ;    // 0.0
        }
    }

}
